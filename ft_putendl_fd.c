#include "libft.h"

void	ft_putendl_fd(char *s, int fd)
{
	char	c;
	int		i;

	i = 0;
	if (!s)
		return ;
	while (s[i] != '\0')
	{
		write(fd, &s[i], 1);
		i++;
	}
	c = '\n';
	write (fd, &c, 1);
}
